def generate_vivado_bridge_for_artifact(
    artifact_dir: str | Path,
    board_name: str = "pynq_z2",
    run_impl_default: bool = False,
) -> Dict[str, Any]:
    artifact = Path(artifact_dir).resolve()
    build_dir = artifact / "build"
    if not build_dir.exists():
        build_dir = artifact
    manifest = _load_json(build_dir / "manifest.json")
    top_name = _top_name_from_manifest(manifest)
    board = get_board(board_name).to_dict()
    hls_dir = _find_hls_dir(build_dir)
    if hls_dir is None:
        raise FileNotFoundError(f"Could not find HLS directory/run_hls.tcl under {build_dir}")

    out_dir = build_dir / "vivado_bridge"
    scripts_dir = out_dir / "scripts"
    reports_dir = out_dir / "reports"
    (out_dir / "hls_ip").mkdir(parents=True, exist_ok=True)
    reports_dir.mkdir(parents=True, exist_ok=True)

    _write(scripts_dir / "export_hls_ip.tcl", _hls_export_tcl(hls_dir, out_dir, top_name, board["part"]))
    _write(scripts_dir / "create_bd.tcl", _create_bd_tcl(board["name"], top_name, board.get("ps_type", "processing_system7")))
    _write(scripts_dir / "run_vivado.tcl", _run_vivado_tcl(board, top_name, run_impl_default))
    _write(out_dir / "README_VIVADO.md", _readme(board, top_name))

    artifact = {
        "vivado_bridge_generated": True,
        "vivado_tcl_generated": True,
        "hls_export_tcl_generated": True,
        "vivado_project_tcl_generated": True,
        "block_design_tcl_generated": True,
        "board": board["name"],
        "part": board["part"],
        "board_part": board.get("board_part"),
        "ps_type": board.get("ps_type"),
        "overlay_style": board.get("overlay_style"),
        "supports_bridge_generation": board.get("supports_bridge_generation", True),
        "supports_hls_ip_export": board.get("supports_hls_ip_export", True),
        "supports_vivado_synth": board.get("supports_vivado_synth", True),
        "supports_vivado_impl": board.get("supports_vivado_impl", True),
        "top_name": top_name,
        "artifact_dir": artifact.as_posix(),
        "build_dir": build_dir.as_posix(),
        "hls_dir": hls_dir.as_posix(),
        "vivado_bridge_dir": out_dir.as_posix(),
        "hls_ip_expected": True,
        "hls_ip_exported": False,
        "vivado_synth_requested": True,
        "vivado_impl_requested": bool(run_impl_default),
        "bitstream_requested": bool(run_impl_default),
        "reports_expected": [
            "utilization_synth.rpt",
            "timing_synth.rpt",
            "power_synth.rpt",
            "utilization_impl.rpt",
            "timing_impl.rpt",
            "power_impl.rpt",
        ],
        "energy_estimation": {
            "enabled_after_power_report": True,
            "formula": "estimated_energy_j = vivado_total_power_w * hls_latency_s",
            "status": "pending_reports",
        },
    }
    _write(out_dir / "vivado_bridge_manifest.json", json.dumps(artifact, indent=2, sort_keys=True))
    return artifact


def _iter_artifacts(experiment_dir: Path) -> Iterable[Path]:
    artifacts = experiment_dir / "artifacts"
    if artifacts.exists():
        for p in sorted(artifacts.iterdir()):
            if p.is_dir():
                yield p
    else:
        # Allow direct artifact/build paths for debugging.
        yield experiment_dir


def generate_vivado_bridge_for_experiment(
    experiment_dir: str | Path,
    board_name: str = "pynq_z2",
    run_impl_default: bool = False,
) -> List[Dict[str, Any]]:
    exp = Path(experiment_dir).resolve()
    rows: List[Dict[str, Any]] = []
    for art in _iter_artifacts(exp):
        try:
            ev = generate_vivado_bridge_for_artifact(art, board_name=board_name, run_impl_default=run_impl_default)
            ev["design"] = art.name
            ev["status"] = "generated"
        except Exception as exc:  # keep artifact-driven behavior across all records
            ev = {
                "design": art.name,
                "status": "error",
                "vivado_bridge_generated": False,
                "error": str(exc),
                "artifact_dir": art.as_posix(),
            }
        rows.append(ev)
    out = exp / "vivado_bridge_artifact.json"
    _write(out, json.dumps(rows, indent=2, sort_keys=True))
    return rows
