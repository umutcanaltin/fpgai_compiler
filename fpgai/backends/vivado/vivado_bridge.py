def emit_vivado_project_handoff(
    out_dir: str | Path,
    *,
    raw_config: Optional[Dict[str, Any]] = None,
    build_stages: Optional[Dict[str, Any]] = None,
    pipeline_mode: str = "inference",
    top_name: str = "deeplearn",
    hls_dir: Optional[str | Path] = None,
    runtime_sequence: Optional[Dict[str, Any]] = None,
    source_ports: Optional[Dict[str, Any]] = None,
) -> Optional[Dict[str, str]]:
    """Emit direct Vivado project/BD Tcl handoff files for compiler builds.

    This is generation/structural validation only. It does not run Vivado and it
    must only produce Tcl when build.stages.vivado_project is requested.
    """
    stages = build_stages or {}
    reports_dir = Path(out_dir) / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)
    if not bool(stages.get("vivado_project")):
        payload = {
            "format": "fpgai.vivado_bd_validation.v1",
            "status": "not_requested",
            "vivado_project_requested": False,
            "truth_boundary": "Vivado Tcl generation was not requested by build.stages.vivado_project.",
        }
        json_path = reports_dir / "vivado_bd_validation.json"
        md_path = reports_dir / "vivado_bd_validation.md"
        json_path.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")
        md_path.write_text("# Vivado BD validation\n\n- status: `not_requested`\n", encoding="utf-8")
        return {"vivado_bd_validation_json": str(json_path), "vivado_bd_validation_md": str(md_path)}

    raw = raw_config or {}
    board_name = str(
        raw.get("targets", {}).get("board")
        or raw.get("targets", {}).get("platform", {}).get("board")
        or raw.get("project", {}).get("board")
        or "pynq_z2"
    )
    board = get_board(board_name).to_dict()
    features = _vivado_requested_features(runtime_sequence=runtime_sequence, source_ports=source_ports, pipeline_mode=pipeline_mode, raw_config=raw)
    vivado_dir = Path(out_dir) / "vivado"
    vivado_dir.mkdir(parents=True, exist_ok=True)
    (vivado_dir / "hls_ip").mkdir(parents=True, exist_ok=True)
    (vivado_dir / "reports").mkdir(parents=True, exist_ok=True)
    project_tcl = vivado_dir / "project.tcl"
    bd_tcl = vivado_dir / "bd.tcl"
    run_tcl = vivado_dir / "run_vivado.tcl"
    project_tcl.write_text(_vivado_project_tcl(board, top_name), encoding="utf-8")
    bd_tcl.write_text(_vivado_direct_bd_tcl(board, top_name, features=features), encoding="utf-8")
    run_tcl.write_text(_vivado_run_tcl(stages), encoding="utf-8")

    bd_source = bd_tcl.read_text(encoding="utf-8")
    checks = {
        "project_tcl": project_tcl.exists(),
        "bd_tcl": bd_tcl.exists(),
        "run_vivado_tcl": run_tcl.exists(),
        "ps_block": ("processing_system7" in bd_source) or ("zynq_ultra_ps_e" in bd_source),
        "hls_ip_instance": "create_bd_cell -type ip" in bd_source and "hls_ip_defs" in bd_source,
        "axi_lite_control_path": "axi_ctrl_interconnect" in bd_source and "s_axi_control" in bd_source,
        "axi_dma_required": bool(features.get("axi_dma")),
        "axi_dma_present": "axi_dma_0" in bd_source,
        "m_axi_memory_required": bool(features.get("m_axi_memory")),
        "m_axi_memory_present": "axi_mem_interconnect" in bd_source,
        "gradient_path_requested": bool(features.get("export_gradients")),
        "optimizer_state_path_requested": bool(features.get("export_optimizer_state")),
    }
    failures: List[str] = []
    for name in ["project_tcl", "bd_tcl", "run_vivado_tcl", "ps_block", "hls_ip_instance", "axi_lite_control_path"]:
        if not checks.get(name):
            failures.append(name)
    if checks["axi_dma_required"] and not checks["axi_dma_present"]:
        failures.append("axi_dma_missing")
    if not checks["axi_dma_required"] and checks["axi_dma_present"]:
        failures.append("axi_dma_unrequested_present")
    if checks["m_axi_memory_required"] and not checks["m_axi_memory_present"]:
        failures.append("m_axi_memory_missing")
    status = "passed" if not failures else "failed"
    payload = {
        "format": "fpgai.vivado_bd_validation.v1",
        "status": status,
        "vivado_project_requested": True,
        "build_stages": {str(k): bool(v) for k, v in stages.items()},
        "board": board["name"],
        "part": board["part"],
        "ps_type": board.get("ps_type"),
        "top_name": top_name,
        "hls_dir": str(hls_dir) if hls_dir is not None else None,
        "vivado_dir": str(vivado_dir),
        "generated_files": {
            "project_tcl": str(project_tcl),
            "bd_tcl": str(bd_tcl),
            "run_vivado_tcl": str(run_tcl),
        },
        "requested_features": features,
        "checks": checks,
        "failures": failures,
        "truth_boundary": "This is structural Tcl generation/validation. It is not proof that Vivado synthesis, implementation, bitstream, or FPGA execution succeeded.",
    }
    json_path = reports_dir / "vivado_bd_validation.json"
    md_path = reports_dir / "vivado_bd_validation.md"
    json_path.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")
    lines = [
        "# Vivado BD validation",
        "",
        f"- status: `{status}`",
        f"- board: `{board['name']}`",
        f"- ps_type: `{board.get('ps_type')}`",
        f"- top_name: `{top_name}`",
        f"- vivado_project_requested: `True`",
        "",
        "## Generated files",
        f"- `{project_tcl}`",
        f"- `{bd_tcl}`",
        f"- `{run_tcl}`",
        "",
        "## Requested hardware paths",
        f"- axi_dma: `{features.get('axi_dma')}`",
        f"- m_axi_memory: `{features.get('m_axi_memory')}`",
        f"- export_gradients: `{features.get('export_gradients')}`",
        f"- export_optimizer_state: `{features.get('export_optimizer_state')}`",
        "",
        "This report validates generated Tcl structure only; Vivado execution is a later verification level.",
        "",
    ]
    md_path.write_text("\n".join(lines), encoding="utf-8")
    return {
        "vivado_project_tcl": str(project_tcl),
        "vivado_bd_tcl": str(bd_tcl),
        "vivado_run_tcl": str(run_tcl),
        "vivado_bd_validation_json": str(json_path),
        "vivado_bd_validation_md": str(md_path),
    }


def _vivado_tool_status(raw_config: Optional[Dict[str, Any]] = None) -> Dict[str, Any]:
    """Return honest Vivado executable availability without running Vivado."""
    raw = raw_config or {}
    toolchain = raw.get("toolchain", {}) if isinstance(raw.get("toolchain", {}), dict) else {}
    vivado_cfg = toolchain.get("vivado", {}) if isinstance(toolchain.get("vivado", {}), dict) else {}
    configured = vivado_cfg.get("executable") or vivado_cfg.get("path") or os.environ.get("FPGAI_VIVADO") or "vivado"
    executable = str(configured)
    resolved = shutil.which(executable) if executable else None
    return {
        "executable": executable,
        "resolved_path": resolved,
        "available": resolved is not None,
    }


def _find_first_existing(base: Path, patterns: Iterable[str]) -> Optional[Path]:
    for pattern in patterns:
        for candidate in sorted(base.glob(pattern)):
            try:
                if candidate.exists() and candidate.is_file():
                    return candidate
            except OSError:
                continue
    return None


def _write_truth_report(path_json: Path, path_md: Path, payload: Dict[str, Any], *, title: str) -> Dict[str, str]:
    path_json.parent.mkdir(parents=True, exist_ok=True)
    path_md.parent.mkdir(parents=True, exist_ok=True)
    path_json.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")
    lines = [
        f"# {title}",
        "",
        f"- status: `{payload.get('status')}`",
        f"- stage: `{payload.get('stage')}`",
        f"- requested: `{payload.get('requested')}`",
        f"- claimed_success: `{payload.get('claimed_success')}`",
    ]
    reason = payload.get("reason")
    if reason:
        lines.append(f"- reason: {reason}")
    tool = payload.get("vivado_tool")
    if isinstance(tool, dict):
        lines.extend([
            "",
            "## Vivado tool",
            f"- executable: `{tool.get('executable')}`",
            f"- resolved_path: `{tool.get('resolved_path')}`",
            f"- available: `{tool.get('available')}`",
        ])
    artifact = payload.get("artifact")
    if artifact:
        lines.extend(["", "## Artifact", f"- `{artifact}`"])
    lines.extend(["", str(payload.get("truth_boundary", "No success is claimed without real tool/artifact evidence.")), ""])
    path_md.write_text("\n".join(lines), encoding="utf-8")
    return {path_json.stem + "_json": str(path_json), path_md.stem + "_md": str(path_md)}


def emit_vivado_truth_reports(
    out_dir: str | Path,
    *,
    raw_config: Optional[Dict[str, Any]] = None,
    build_stages: Optional[Dict[str, Any]] = None,
    vivado_handoff_artifacts: Optional[Dict[str, str]] = None,
    board_fit_artifacts: Optional[Dict[str, Any]] = None,
) -> Dict[str, str]:
    """Emit Sprint E Vivado execution/implementation/bitstream truth reports.

    This function does not run Vivado. It records whether the requested Vivado
    verification level has real tool/artifact evidence or must remain unclaimed.
    """
    root = Path(out_dir)
    reports_dir = root / "reports"
    vivado_dir = root / "vivado"
    reports_dir.mkdir(parents=True, exist_ok=True)
    stages = build_stages or {}
    tool = _vivado_tool_status(raw_config)
    handoff_present = bool(vivado_handoff_artifacts and vivado_handoff_artifacts.get("vivado_bd_tcl"))
    board_fit = board_fit_artifacts or {}
    board_fit_status = str(board_fit.get("status") or "unknown")
    board_fit_allows_impl = bool(board_fit.get("vivado_implementation_allowed", board_fit.get("vivado_allowed", True)))
    board_fit_allows_bitstream = bool(board_fit.get("bitstream_allowed", board_fit.get("vivado_allowed", True)))
    board_fit_json = board_fit.get("json") or board_fit.get("board_fit_json")

    validation_requested = bool(stages.get("vivado_project"))
    if not validation_requested:
        validation_status = "not_requested"
        validation_reason = "build.stages.vivado_project is false."
    elif not tool["available"]:
        validation_status = "tool_missing"
        validation_reason = "Vivado executable was not found; generated Tcl was not executed."
    else:
        validation_log = _find_first_existing(vivado_dir, ["reports/*validation*.log", "reports/*validate*.rpt", "project/*.jou"])
        if validation_log is not None:
            validation_status = "passed"
            validation_reason = "Vivado validation artifact was found."
        else:
            validation_status = "not_run"
            validation_reason = "Vivado Tcl was generated, but no Vivado validation log/report was found."
    validation_payload = {
        "format": "fpgai.vivado_validation_report.v1",
        "stage": "vivado_project_validation",
        "requested": validation_requested,
        "status": validation_status,
        "reason": validation_reason,
        "claimed_success": validation_status == "passed",
        "vivado_tool": tool,
        "vivado_handoff_present": handoff_present,
        "vivado_handoff_artifacts": vivado_handoff_artifacts or {},
        "truth_boundary": "This report only claims Vivado validation success when a real Vivado validation artifact exists. Generated Tcl/structural validation alone is not Vivado execution proof.",
    }

    impl_requested = bool(stages.get("vivado_implementation"))
    impl_artifact = _find_first_existing(vivado_dir, [
        "reports/*impl*.rpt",
        "reports/*implementation*.rpt",
        "project/**/*.dcp",
        "project/**/*.jou",
    ])
    if not impl_requested:
        impl_status = "not_requested"
        impl_reason = "build.stages.vivado_implementation is false."
    elif not board_fit_allows_impl:
        impl_status = "blocked_by_board_fit"
        impl_reason = f"Vivado implementation was requested but board_fit.status={board_fit_status!r} does not allow implementation for the selected board."
    elif not tool["available"]:
        impl_status = "tool_missing"
        impl_reason = "Vivado executable was not found on PATH/configured location."
    elif impl_artifact is None:
        impl_status = "artifact_missing"
        impl_reason = "Vivado implementation was requested but no implementation report/checkpoint artifact was found."
    else:
        impl_status = "passed"
        impl_reason = "Vivado implementation artifact was found."
    impl_payload = {
        "format": "fpgai.vivado_implementation_report.v1",
        "stage": "vivado_implementation",
        "requested": impl_requested,
        "status": impl_status,
        "reason": impl_reason,
        "claimed_success": impl_status == "passed",
        "vivado_tool": tool,
        "artifact": str(impl_artifact) if impl_artifact is not None else None,
        "requires_vivado_project": True,
        "vivado_project_requested": validation_requested,
        "board_fit_status": board_fit_status,
        "board_fit_json": board_fit_json,
        "board_fit_allowed": board_fit_allows_impl,
        "truth_boundary": "Vivado implementation success is claimed only when board-fit allows the stage and real implementation reports/checkpoints are present. Missing tools/artifacts are not converted into success.",
    }

    bit_requested = bool(stages.get("bitstream"))
    bit_artifact = _find_first_existing(vivado_dir, ["**/*.bit", "**/*.xsa"])
    if not bit_requested:
        bit_status = "not_requested"
        bit_reason = "build.stages.bitstream is false."
    elif not board_fit_allows_bitstream:
        bit_status = "blocked_by_board_fit"
        bit_reason = f"Bitstream was requested but board_fit.status={board_fit_status!r} does not allow bitstream generation for the selected board."
    elif impl_status != "passed":
        bit_status = "artifact_missing" if tool["available"] else "tool_missing"
        bit_reason = "Bitstream was requested but Vivado implementation has not passed with real artifacts."
    elif bit_artifact is None:
        bit_status = "artifact_missing"
        bit_reason = "Bitstream was requested but no .bit/.xsa artifact was found."
    else:
        bit_status = "passed"
        bit_reason = "Bitstream/XSA artifact was found."
    bit_payload = {
        "format": "fpgai.bitstream_report.v1",
        "stage": "bitstream",
        "requested": bit_requested,
        "status": bit_status,
        "reason": bit_reason,
        "claimed_success": bit_status == "passed",
        "vivado_tool": tool,
        "artifact": str(bit_artifact) if bit_artifact is not None else None,
        "requires_vivado_implementation_passed": True,
        "vivado_implementation_status": impl_status,
        "board_fit_status": board_fit_status,
        "board_fit_json": board_fit_json,
        "board_fit_allowed": board_fit_allows_bitstream,
        "truth_boundary": "Bitstream success is claimed only when board-fit allows the stage, implementation has passed, and a real .bit or .xsa artifact exists.",
    }

    artifacts: Dict[str, str] = {}
    artifacts.update(_write_truth_report(reports_dir / "vivado_validation_report.json", reports_dir / "vivado_validation_report.md", validation_payload, title="Vivado validation report"))
    artifacts.update(_write_truth_report(reports_dir / "vivado_implementation_report.json", reports_dir / "vivado_implementation_report.md", impl_payload, title="Vivado implementation report"))
    artifacts.update(_write_truth_report(reports_dir / "bitstream_report.json", reports_dir / "bitstream_report.md", bit_payload, title="Bitstream report"))
    return artifacts

def generate_vivado_bridge_for_artifact(
    artifact_dir: str | Path,
    board_name: str = "pynq_z2",
    run_impl_default: bool = False,
) -> Dict[str, Any]:
    artifact = Path(artifact_dir).resolve()
    build_dir = artifact / "build"
    if not build_dir.exists():
        build_dir = artifact
    manifest = _load_json(build_dir / "manifest.json")
    top_name = _top_name_from_manifest(manifest)
    board = get_board(board_name).to_dict()
    hls_dir = _find_hls_dir(build_dir)
    if hls_dir is None:
        raise FileNotFoundError(f"Could not find HLS directory/run_hls.tcl under {build_dir}")

    out_dir = build_dir / "vivado_bridge"
    scripts_dir = out_dir / "scripts"
    reports_dir = out_dir / "reports"
    (out_dir / "hls_ip").mkdir(parents=True, exist_ok=True)
    reports_dir.mkdir(parents=True, exist_ok=True)

    _write(scripts_dir / "export_hls_ip.tcl", _hls_export_tcl(hls_dir, out_dir, top_name, board["part"]))
    _write(scripts_dir / "create_bd.tcl", _create_bd_tcl(board["name"], top_name, board.get("ps_type", "processing_system7")))
    _write(scripts_dir / "run_vivado.tcl", _run_vivado_tcl(board, top_name, run_impl_default))
    _write(out_dir / "README_VIVADO.md", _readme(board, top_name))

    artifact = {
        "vivado_bridge_generated": True,
        "vivado_tcl_generated": True,
        "hls_export_tcl_generated": True,
        "vivado_project_tcl_generated": True,
        "block_design_tcl_generated": True,
        "board": board["name"],
        "part": board["part"],
        "board_part": board.get("board_part"),
        "ps_type": board.get("ps_type"),
        "overlay_style": board.get("overlay_style"),
        "supports_bridge_generation": board.get("supports_bridge_generation", True),
        "supports_hls_ip_export": board.get("supports_hls_ip_export", True),
        "supports_vivado_synth": board.get("supports_vivado_synth", True),
        "supports_vivado_impl": board.get("supports_vivado_impl", True),
        "top_name": top_name,
        "artifact_dir": artifact.as_posix(),
        "build_dir": build_dir.as_posix(),
        "hls_dir": hls_dir.as_posix(),
        "vivado_bridge_dir": out_dir.as_posix(),
        "hls_ip_expected": True,
        "hls_ip_exported": False,
        "vivado_synth_requested": True,
        "vivado_impl_requested": bool(run_impl_default),
        "bitstream_requested": bool(run_impl_default),
        "reports_expected": [
            "utilization_synth.rpt",
            "timing_synth.rpt",
            "power_synth.rpt",
            "utilization_impl.rpt",
            "timing_impl.rpt",
            "power_impl.rpt",
        ],
        "energy_estimation": {
            "enabled_after_power_report": True,
            "formula": "estimated_energy_j = vivado_total_power_w * hls_latency_s",
            "status": "pending_reports",
        },
    }
    _write(out_dir / "vivado_bridge_manifest.json", json.dumps(artifact, indent=2, sort_keys=True))
    return artifact


def _iter_artifacts(experiment_dir: Path) -> Iterable[Path]:
    artifacts = experiment_dir / "artifacts"
    if artifacts.exists():
        for p in sorted(artifacts.iterdir()):
            if p.is_dir():
                yield p
    else:
        # Allow direct artifact/build paths for debugging.
        yield experiment_dir


def generate_vivado_bridge_for_experiment(
    experiment_dir: str | Path,
    board_name: str = "pynq_z2",
    run_impl_default: bool = False,
) -> List[Dict[str, Any]]:
    exp = Path(experiment_dir).resolve()
    rows: List[Dict[str, Any]] = []
    for art in _iter_artifacts(exp):
        try:
            ev = generate_vivado_bridge_for_artifact(art, board_name=board_name, run_impl_default=run_impl_default)
            ev["design"] = art.name
            ev["status"] = "generated"
        except Exception as exc:  # keep artifact-driven behavior across all records
            ev = {
                "design": art.name,
                "status": "error",
                "vivado_bridge_generated": False,
                "error": str(exc),
                "artifact_dir": art.as_posix(),
            }
        rows.append(ev)
    out = exp / "vivado_bridge_artifact.json"
    _write(out, json.dumps(rows, indent=2, sort_keys=True))
    return rows
