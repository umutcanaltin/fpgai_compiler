def _runtime_commands(runtime_sequence: Optional[Dict[str, Any]]) -> List[str]:
    seq = runtime_sequence.get("sequence", []) if isinstance(runtime_sequence, dict) else []
    out: List[str] = []
    for item in seq:
        if isinstance(item, str):
            out.append(item)
        elif isinstance(item, dict):
            cmd = item.get("command")
            if isinstance(cmd, str):
                out.append(cmd)
            elif len(item) == 1:
                key = next(iter(item.keys()))
                if isinstance(key, str):
                    out.append(key)
    return out


def _data_movement_requests_axi_dma(raw_config: Optional[Dict[str, Any]]) -> bool:
    """Return True only when YAML explicitly selects AXI-stream/DMA movement.

    Generated C++ may contain host/testbench stream-looking ports for internal or
    compatibility reasons. Direct Vivado handoff must not instantiate DMA just
    because those names appear: DMA consumes resources and must be tied to a real
    user/compiler movement decision.
    """
    dm = (raw_config or {}).get("data_movement", {})
    if not isinstance(dm, dict):
        return False

    def wants_dma(node: Any) -> bool:
        if isinstance(node, dict):
            interface = str(node.get("interface", "")).lower()
            transport = str(node.get("transport", "")).lower()
            if interface in {"axi_stream", "axis"} or transport == "dma":
                return True
            return any(wants_dma(v) for v in node.values())
        if isinstance(node, list):
            return any(wants_dma(v) for v in node)
        return False

    return wants_dma(dm)


def _vivado_requested_features(
    *,
    runtime_sequence: Optional[Dict[str, Any]],
    source_ports: Optional[Dict[str, Any]],
    pipeline_mode: str,
    raw_config: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    commands = set(_runtime_commands(runtime_sequence))
    m_axi_ports = sorted(str(p) for p in (source_ports or {}).get("m_axi_ports", []) or [])
    axis_ports = sorted(str(p) for p in (source_ports or {}).get("axis_ports", []) or [])
    lowered_ports = {p.lower() for p in m_axi_ports + axis_ports}
    axi_dma_requested = _data_movement_requests_axi_dma(raw_config)
    return {
        "commands": sorted(commands),
        "m_axi_ports": m_axi_ports,
        "axis_ports": axis_ports,
        "axi_dma": bool(axis_ports) and axi_dma_requested,
        "axi_dma_decision_source": "data_movement_axi_stream_or_dma" if axi_dma_requested else "not_requested_by_data_movement",
        "m_axi_memory": bool(m_axi_ports),
        "training_aux": str(pipeline_mode).lower() == "training_on_device" and bool(axis_ports) and axi_dma_requested,
        "import_weights": "import_weights" in commands or any("weight" in p for p in lowered_ports),
        "export_weights": "export_weights" in commands,
        "export_gradients": "export_gradients" in commands or any("gradient" in p for p in lowered_ports),
        "export_optimizer_state": "export_optimizer_state" in commands or any("optimizer" in p for p in lowered_ports),
    }


