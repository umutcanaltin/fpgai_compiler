"""Generate Vivado bridge artifacts for FPGAI HLS outputs.

Vivado bridge intentionally separates script generation from full hardware
implementation. It creates reproducible Tcl scripts for:

* exporting an existing Vitis HLS project as Vivado IP;
* creating a Vivado project and block-design skeleton;
* running synthesis/implementation/report commands when explicitly enabled;
* extracting report/artifact metadata later.
"""

from __future__ import annotations

